package Assignment.OverLoading;

public class OverLoading {
    public int sum(int a, int b){
        return a + b;
    }
    public double sum(double a, double b){
        return a + b;
    }
    public int diff(int a, int b){
        return a - b;
    }
    public double diff(double a, double b){
        return a - b;
    }
}
